<h2 class="wp-block-heading">What is a Mortgage?</h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>A mortgage is a long-term loan provided by banks and other financial institutions, where the property serves as loan collateral. Simply put, when you obtain a mortgage to buy a house, the property acts as collateral until the loan is fully repaid.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Mortgage loans differ from other types of credit because they tend to have lower interest rates and longer repayment terms due to the security provided by the real estate collateral. However, this also means that the approval process tends to be more rigorous, requiring a thorough evaluation of your personal finances.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Mortgages are one of the most common ways to finance home ownership, and understanding them is essential to managing this long-term financial commitment.</p>
